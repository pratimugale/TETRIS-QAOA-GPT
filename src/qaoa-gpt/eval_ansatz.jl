"""
    eval_ansatz(formula_data, q_circuit, n_nodes, op_pool)

Evaluate the energy of a quantum circuit against a Max-3-SAT Hamiltonian.
- `formula_data`: List of lists of dictionaries representing the Max-3-SAT formula.
- `q_circuit`: Flattened list of predicted tokens in Tetris format
- `n_nodes`: Number of qubits (variables).
- `op_pool`: Name of the operator pool.
"""
function eval_ansatz(
    formula_data,
    q_circuit,
    n_nodes,
    op_pool,
)
    # 1. Initialize Operator Pool
    if op_pool == "qaoa_mixer"
        pool = ADAPT.ADAPT_QAOA.QAOApools.qaoa_mixer(n_nodes)
    elseif op_pool == "qaoa_double_pool"
        pool = ADAPT.ADAPT_QAOA.QAOApools.qaoa_double_pool(n_nodes)
    elseif op_pool == "qaoa_nondiagonal_double_pool"
        pool = ADAPT.ADAPT_QAOA.QAOApools.qaoa_nondiagonal_double_pool(n_nodes)
    else
        throw(error("op_pool $(op_pool) is not supported for Max-3-SAT evaluation."))
    end

    # 2. Construct Exact Max-3-SAT Hamiltonian
    # formula_data is expected to be a Vector{Vector{Int}} (list of clauses)
    formula = ADAPT.Hamiltonians.Max3SAT.Types.get_formula_from_list(formula_data)
    H_spv = ADAPT.Hamiltonians.Max3SAT.get_exact_hamiltonian(formula, n_nodes)
    
    # Wrap in a QAOAObservable view for ADAPT.jl compatibility
    H = ADAPT.ADAPT_QAOA.QAOAObservable(H_spv)
    
    # 3. Initialize Starting State |+⟩ (Equal superposition)
    ψ0 = ones(ComplexF64, 2^n_nodes) / sqrt(2^n_nodes)
    ψ0 /= norm(ψ0)
    
    op_indices = []
    gammas = Float64[]
    betas = Float64[]
    
    generated_list = q_circuit
    
    # 4. Extract operators and angles (State-Machine Parser)
    i = 1
    while i <= length(generated_list)
        if generated_list[i] == "new_layer_p"
            i += 1
            # Find the next layer marker or end of list
            next_marker = i
            while next_marker <= length(generated_list) && generated_list[next_marker] != "new_layer_p"
                next_marker += 1
            end
            
            # The tokens for this layer
            layer_tokens = generated_list[i:next_marker-1]
            if length(layer_tokens) >= 3 # Min: [op1, beta1, gamma]
                # Last token MUST be a float (gamma)
                gamma_token = layer_tokens[end]
                if !(gamma_token isa Number)
                    error("Malformed layer: Final token in layer $(layer_tokens) is not a numeric gamma.")
                end
                push!(gammas, Float64(gamma_token))
                
                # Preceding tokens MUST be pairs of (Int op, Number beta)
                op_beta_pairs = layer_tokens[1:end-1]
                if length(op_beta_pairs) % 2 != 0
                    error("Malformed layer: Found odd number of tokens $(op_beta_pairs) before gamma. Expected (op, beta) pairs.")
                end
                
                layer_ops = Int[]
                for k in 1:2:length(op_beta_pairs)
                    op_idx = op_beta_pairs[k]
                    beta_val = op_beta_pairs[k+1]
                    
                    if !(op_idx isa Integer) || !(beta_val isa Number)
                        error("Malformed layer: Invalid (op, beta) pair: ($op_idx, $beta_val)")
                    end
                    
                    push!(layer_ops, Int(op_idx))
                    push!(betas, Float64(beta_val))
                end
                push!(op_indices, layer_ops)
            else
                if length(layer_tokens) > 0
                    error("Malformed layer: Layer sequence $(layer_tokens) is too short.")
                end
            end
            i = next_marker
        else
            # Ignore stray tokens until we hit a marker
            i += 1
        end
    end

    if length(gammas) == 0
        error("No valid layers found in circuit.")
    end

    # Flatten angles for ADAPT.bind! [gammas..., betas...]
    angles = vcat(gammas, betas)

    # 5. Reconstruct the Ansatz
    # We use a TetrisQAOAAnsatz for consistency with the training data generation (run_tetris.jl)
    # The initial gamma value of 0.5 is the default value, we however later set the optimized gammas
    #    generated by QAOA-GPT or actual ground truth QAOA optimization.
    ansatz = ADAPT.ADAPT_QAOA.TetrisQAOAAnsatz(0.5, pool, H)

    for layer_ops in op_indices
        # Store current param count to mark the start of the layer for HP insertion
        p_current = length(ansatz.parameters)
        for op_idx in layer_ops
            if op_idx > length(pool) || op_idx < 1
                 error("Predicted operator index $(op_idx) is out of bounds for pool of size $(length(pool)).")
            end
            # This is the Tetris tiling bit: push only the mixers. bind! will set angles later.
            push!(ansatz.generators, pool[op_idx])
            push!(ansatz.parameters, 0.0)
        end
        
        # In TetrisQAOAAnsatz, we insert HP at the start of the layer
        push!(ansatz.γ_values, ansatz.γ0) # placeholder that will be overwritten by bind!
        push!(ansatz.γ_layers, 1 + p_current)
    end

    # Bind the predicted angles ([gammas..., betas...])
    ADAPT.bind!(ansatz, angles)

    # 6. Evolve and Evaluate Final Energy
    # final_energy = <psi|H|psi>
    ψEND = ADAPT.evolve_state(ansatz, ψ0)
    E_final = ADAPT.evaluate(H, ψEND)
    
    return E_final
end